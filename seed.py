def seed_database():
    if Employee.query.first():
        print("Database already seeded.")
        return

    portfolio_cache = {}
    all_portfolio_names = set()
    for emp_data in EMPLOYEES_DATA:
        for p in emp_data["portfolios"]:
            all_portfolio_names.add(p)
        for _, p_port in emp_data["projects"]:
            all_portfolio_names.add(p_port)

    for name in all_portfolio_names:
        p = Portfolio(name=name)
        db.session.add(p)
        db.session.flush()
        portfolio_cache[name] = p

    for t_num, (q_text, g_link) in enumerate(WEEKLY_TASKS, 1):
        td = TaskDefinition(task_number=t_num, question_text=q_text, goal_link=g_link)
        db.session.add(td)

    for emp_data in EMPLOYEES_DATA:
        emp = Employee(name=emp_data["name"])
        db.session.add(emp)
        db.session.flush()

        for port_name in emp_data["portfolios"]:
            emp.portfolios.append(portfolio_cache[port_name])

        for g_num, g_text in enumerate(emp_data["goals"], 1):
            goal = AnnualGoal(employee_id=emp.id, goal_number=g_num, text=g_text, weight="20%")
            db.session.add(goal)

        for proj_name, proj_port in emp_data["projects"]:
            proj = Project(name=proj_name, employee_id=emp.id, portfolio_id=portfolio_cache[proj_port].id)
            db.session.add(proj)

    db.session.commit()
    print(f"Seeded: {Employee.query.count()} employees, {Portfolio.query.count()} portfolios, {Project.query.count()} projects, {TaskDefinition.query.count()} task definitions, {AnnualGoal.query.count()} goals")
